int add(int, int);
int sub(int, int);
int mul(int, int);
int div(int, int);
int mod(int, int);

/**
 * add - addition
 * @a: a
 * @b: b
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction
 * @a: a
 * @b: b
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply
 * @a: a
 * @b: b
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division
 * @a: a
 * @b: b
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus
 * @a: a
 * @b: b
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
